"""
Структурированные промпты для универсального цикла анализа кода.
"""
from typing import Dict, Any, List, Optional


def build_analysis_planning_prompt(
    goal: str,
    analysis_state: Dict[str, Any],
    available_skills: List[Dict[str, Any]],
    step_number: int,
    max_steps: int
) -> str:
    """Промпт для планирования следующего шага анализа."""
    skills_summary = "\n".join([
        f"- Навык '{skill['name']}':\n  " + "\n  ".join([
            f"• {cap['name']}: {cap['description']}"
            for cap in skill.get('capabilities', [])[:3]
        ])
        for skill in available_skills
    ])
    
    return f"""
ПЛАНИРОВАНИЕ ШАГА АНАЛИЗА КОДА

ЦЕЛЬ АНАЛИЗА:
{goal}

ТИП АНАЛИЗА:
{analysis_state.get('analysis_type', 'general_analysis')}

ТЕКУЩЕЕ СОСТОЯНИЕ:
- Выполнено шагов: {step_number} из {max_steps}
- Уверенность в результате: {analysis_state.get('confidence', 0.0):.2f}
- Контекст ошибки: {analysis_state.get('error_context', 'не указан')}

ДОСТУПНЫЕ НАВЫКИ И ВОЗМОЖНОСТИ:
{skills_summary}

ИНСТРУКЦИИ:
1. ОПРЕДЕЛИ следующее наиболее полезное действие для продвижения анализа
2. ВЫБЕРИ подходящий навык и capability из списка доступных
3. СФОРМУЛИРУЙ конкретные параметры для вызова capability
4. УЧТИ ограничение по шагам: осталось {max_steps - step_number} шагов
5. ЕСЛИ анализ завершен или достигнута достаточная уверенность (>0.85) — предложи завершение

ТИПЫ ДЕЙСТВИЙ:
- navigate: навигация к элементу кода (класс, метод, функция)
- search: поиск элементов по имени или паттерну
- analyze_metadata: анализ метаданных элемента кода
- generate_fix: генерация предложения по исправлению
- evaluate_completion: оценка завершения анализа

ФОРМАТ ВЫВОДА: СТРОГО валидный JSON по схеме.
НЕ ДОБАВЛЯЙ пояснений вне JSON.
"""


def build_step_execution_prompt(
    goal: str,
    analysis_type: str,
    action_plan: Dict[str, Any],
    execution_result: Dict[str, Any],
    previous_steps: int,
    max_steps: int,
    current_confidence: float
) -> str:
    """Промпт для оценки результата шага и принятия решения."""
    return f"""
ОЦЕНКА РЕЗУЛЬТАТА ШАГА АНАЛИЗА

ЦЕЛЬ АНАЛИЗА:
{goal}

ТИП АНАЛИЗА:
{analysis_type}

ВЫПОЛНЕННОЕ ДЕЙСТВИЕ:
- Тип: {action_plan.get('action_type', 'unknown')}
- Навык: {action_plan.get('target_skill', 'unknown')}
- Capability: {action_plan.get('capability_name', 'unknown')}
- Параметры: {action_plan.get('parameters', {})}

РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ:
- Статус: {'успех' if execution_result.get('success') else 'неудача'}
- Суммаризация: {execution_result.get('result_summary', 'нет данных')}
- Ошибка: {execution_result.get('error', 'нет')}

КОНТЕКСТ АНАЛИЗА:
- Выполнено шагов: {previous_steps} из {max_steps}
- Текущая уверенность: {current_confidence:.2f}
- Осталось шагов: {max_steps - previous_steps}

ИНСТРУКЦИИ:
1. ОЦЕНИ вклад этого шага в достижение цели анализа
2. ОБНОВИ уровень уверенности в результате (0.0-1.0)
3. ОПРЕДЕЛИ, завершен ли анализ:
   - Завершен если: найдена корневая причина И предложено исправление И уверенность > 0.8
   - Не завершен если: требуется дополнительный анализ или уверенность < 0.8
4. ЕСЛИ не завершен — предложи тип следующего действия
5. ОЦЕНИ необходимость ручной проверки (требуется если уверенность < 0.7 или сложная логика)

ФОРМАТ ВЫВОДА: СТРОГО валидный JSON по схеме.
НЕ ДОБАВЛЯЙ пояснений вне JSON.
"""


def build_final_report_prompt(
    goal: str,
    analysis_type: str,
    analysis_observations: List[Dict[str, Any]],
    error_context: Optional[Dict[str, Any]],
    confidence: float,
    total_steps: int
) -> str:
    """Промпт для генерации финального отчета анализа."""
    # Ограничение объема наблюдений для промпта
    observations_summary = "\n".join([
        f"Шаг {obs.get('step_number', '?')}: {obs.get('action', 'unknown')} — {obs.get('result_summary', obs.get('description', '...'))[:100]}"
        for obs in analysis_observations[:10]
    ])
    
    return f"""
ГЕНЕРАЦИЯ ФИНАЛЬНОГО ОТЧЕТА АНАЛИЗА КОДА

ЦЕЛЬ АНАЛИЗА:
{goal}

ТИП АНАЛИЗА:
{analysis_type}

КОНТЕКСТ ОШИБКИ:
{error_context if error_context else 'Не применимо для данного типа анализа'}

РЕЗУЛЬТАТЫ АНАЛИЗА (последние шаги):
{observations_summary}

СТАТИСТИКА АНАЛИЗА:
- Всего шагов: {total_steps}
- Финальная уверенность: {confidence:.2f}
- Требуется ручная проверка: {'да' if confidence < 0.7 else 'нет'}

ИНСТРУКЦИИ:
1. СФОРМУЛИРУЙ краткое резюме анализа (1-2 предложения)
2. ОПИШИ выявленную проблему или результат анализа
3. УКАЖИ корневую причину (если применимо)
4. ПЕРЕЧИСЛИ ключевые доказательства из анализа
5. ПРЕДЛОЖИ конкретные рекомендации по исправлению или улучшению
6. ОЦЕНИ необходимость ручной проверки
7. ЕСЛИ найдено исправление — предоставь краткий пример кода

ТРЕБОВАНИЯ К ОТЧЕТУ:
- Лаконичность: не более 300 слов
- Конкретика: избегай общих фраз
- Действенность: рекомендации должны быть выполнимы
- Обоснованность: каждое утверждение должно иметь доказательства из анализа

ФОРМАТ ВЫВОДА: СТРОГО валидный JSON по схеме.
НЕ ДОБАВЛЯЙ пояснений вне JSON.
"""


