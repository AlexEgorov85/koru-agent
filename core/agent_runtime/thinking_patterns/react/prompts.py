"""
Промпты для чистой реактивной стратегии без плана.
"""
from typing import Dict, Any, List


def build_react_thought_prompt(
    goal: str,
    current_step: int,
    max_steps: int,
    context_summary: str,
    available_skills: List[Dict[str, Any]]
) -> str:
    """Промпт для формирования мысли (анализ текущего состояния)."""
    skills_text = "\n".join([
        f"- Навык '{skill['name']}':\n  " + "\n  ".join([
            f"• {cap['name']}: {cap['description']}"
            for cap in skill.get('capabilities', [])
        ])
        for skill in available_skills
    ]) or "Нет доступных навыков"
    
    return f"""
РЕАКТИВНЫЙ АНАЛИЗ ТЕКУЩЕГО СОСТОЯНИЯ (ШАГ {current_step + 1} ИЗ {max_steps})

ЦЕЛЬ:
{goal}

ТЕКУЩИЙ КОНТЕКСТ (последние наблюдения):
{context_summary if context_summary else "Нет наблюдений"}

ДОСТУПНЫЕ НАВЫКИ И ДЕЙСТВИЯ:
{skills_text}

ИНСТРУКЦИИ:
1. ПРОАНАЛИЗИРУЙ текущее состояние на основе контекста
2. ОПРЕДЕЛИ, какое действие наиболее полезно для достижения цели СЕЙЧАС
3. ВЫБЕРИ конкретный навык и capability из списка доступных
4. СФОРМУЛИРУЙ параметры для вызова capability
5. ОЦЕНИ уверенность в правильности решения (0.0-1.0)
6. РЕШИ, завершить ли анализ сейчас (если цель достигнута или дальнейшие действия бесполезны)

ТИПЫ ДЕЙСТВИЙ:
- navigate: навигация к элементу кода (файл, класс, метод)
- search: поиск элементов по имени или паттерну
- read_file: чтение содержимого файла
- analyze: анализ структуры или метаданных
- complete: завершение анализа (цель достигнута)

ВАЖНО:
- НЕ планируй будущие шаги — фокусируйся ТОЛЬКО на следующем действии
- Каждое действие должно быть независимым и обоснованным текущим контекстом
- Избегай циклических действий (повторение одних и тех же шагов)

ФОРМАТ ВЫВОДА: СТРОГО валидный JSON по схеме.
НЕ ДОБАВЛЯЙ пояснений вне JSON.
"""


def build_react_action_prompt(
    goal: str,
    thought: Dict[str, Any],
    context_summary: str
) -> str:
    """Промпт для уточнения параметров действия (опционально)."""
    return f"""
УТОЧНЕНИЕ ПАРАМЕТРОВ ДЕЙСТВИЯ

ЦЕЛЬ: {goal}

МЫСЛЬ ДЛЯ ДЕЙСТВИЯ:
{thought.get('reasoning', 'Не указана')}

ТЕКУЩИЙ КОНТЕКСТ:
{context_summary if context_summary else "Нет контекста"}

ЗАДАЧА:
На основе мысли и контекста уточни параметры для выполнения действия.
Верни ТОЛЬКО параметры в формате JSON без дополнительного текста.
"""


