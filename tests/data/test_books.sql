-- Создаём таблицу книг
CREATE TABLE IF NOT EXISTS books (
    id INTEGER PRIMARY KEY,
    title TEXT NOT NULL,
    author TEXT,
    year INTEGER,
    isbn TEXT,
    genre TEXT
);

-- Создаём таблицу авторов
CREATE TABLE IF NOT EXISTS authors (
    id INTEGER PRIMARY KEY,
    first_name TEXT,
    last_name TEXT
);

